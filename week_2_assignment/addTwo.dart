/// Adds two integers and returns the sum.

int addTwo(int num1, int num2) {
  /// Performs the addition of num1 and num2.
  int sum = num1 + num2;

  /// Returns the calculated sum.
  return sum;
}

void main() {
  /// Calls the addTwo function with arguments 2 and 3.
  int result = addTwo(2, 3);

  /// Prints the result of the addition.
  print(result);
}